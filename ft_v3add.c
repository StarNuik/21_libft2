#include "libft.h"

t_vector3	ft_v3add(t_vector3 a, t_vector3 b)
{
	t_vector3	res;

	res.x = a.x + b.x;
	res.y = a.y + b.y;
	res.z = a.z + b.z;
	return (res);
}
